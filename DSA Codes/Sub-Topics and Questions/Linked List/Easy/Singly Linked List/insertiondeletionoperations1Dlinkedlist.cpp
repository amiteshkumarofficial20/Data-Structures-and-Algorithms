/*
==================================================
üìò DETAILED ALGORITHM NOTES ‚Äî LINKED LIST OPERATIONS
==================================================

üß© 1Ô∏è‚É£ NODE STRUCTURE
--------------------------------------------------
Each node in a singly linked list contains:
   - `data`: the actual value stored (integer in this case)
   - `next`: a pointer to the next node (or `nullptr` if it‚Äôs the last node)

üëâ Example:
   [10 | *] ‚Üí [20 | *] ‚Üí [30 | *] ‚Üí NULL

--------------------------------------------------

‚öôÔ∏è 2Ô∏è‚É£ CONVERT ARRAY TO LINKED LIST
--------------------------------------------------
Purpose: Convert a vector of integers `{10, 20, 30, 40}` into a linked list.

Algorithm:
   1. Create the head node with the first element of the array.
   2. Use a pointer `mover` to track the last node.
   3. For every remaining element:
        - Create a new node.
        - Link it to the last node (`mover->next = newNode`).
        - Move the `mover` forward.
   4. Return `head`.

Time Complexity ‚Üí O(N)
Space Complexity ‚Üí O(1) (apart from the list itself)

--------------------------------------------------

üñ®Ô∏è 3Ô∏è‚É£ PRINT LINKED LIST
--------------------------------------------------
Purpose: Traverse and print all elements of the linked list.

Algorithm:
   1. Initialize a temporary pointer `temp = head`.
   2. While `temp` is not NULL:
        - Print `temp->data`.
        - Move to the next node (`temp = temp->next`).

Time Complexity ‚Üí O(N)
Space Complexity ‚Üí O(1)

--------------------------------------------------

‚ûï 4Ô∏è‚É£ INSERTION OPERATIONS
--------------------------------------------------

(A) Insert at Head
------------------
Algorithm:
   1. Create a new node with given value.
   2. Point its `next` to the current head.
   3. Return this new node as the new head.

Effect ‚Üí Adds a node at the start.
Time ‚Üí O(1)

--------------------------------------------------

(B) Insert at Tail
------------------
Algorithm:
   1. Create a new node.
   2. If the list is empty ‚Üí this node becomes head.
   3. Otherwise, traverse till `temp->next == NULL`.
   4. Set `temp->next = newNode`.

Effect ‚Üí Adds node at the end.
Time ‚Üí O(N)

--------------------------------------------------

(C) Insert at Kth Position (1-based)
-------------------------------------
Algorithm:
   1. If k == 1 ‚Üí call `insertAtHead()`.
   2. Traverse the list till (k-1)-th node using counter.
   3. If list ends before reaching (k-1), print ‚ÄúPosition out of range‚Äù.
   4. Otherwise:
        - Create a new node.
        - Set `newNode->next = temp->next`.
        - Update `temp->next = newNode`.

Effect ‚Üí Inserts node at any desired position.
Time ‚Üí O(K)

--------------------------------------------------

(D) Insert After Given Value
-----------------------------
Algorithm:
   1. Traverse until a node with the target `element` is found.
   2. If not found ‚Üí print ‚ÄúElement not found‚Äù.
   3. Otherwise:
        - Create a new node.
        - Set `newNode->next = temp->next`.
        - Update `temp->next = newNode`.

Effect ‚Üí Adds node immediately after a specified value.
Time ‚Üí O(N)

--------------------------------------------------

‚ûñ 5Ô∏è‚É£ DELETION OPERATIONS
--------------------------------------------------

(A) Delete Head
----------------
Algorithm:
   1. If head == NULL ‚Üí return NULL.
   2. Store current head in `temp`.
   3. Move `head = head->next`.
   4. Delete `temp` (to free memory).
   5. Return new head.

Effect ‚Üí Removes the first node.
Time ‚Üí O(1)

--------------------------------------------------

(B) Delete Tail
----------------
Algorithm:
   1. If list is empty or has one node ‚Üí delete and return NULL.
   2. Traverse until the second-last node (`temp->next->next == NULL`).
   3. Delete `temp->next` (last node).
   4. Set `temp->next = NULL`.

Effect ‚Üí Removes the last node.
Time ‚Üí O(N)

--------------------------------------------------

(C) Delete Node at Kth Position
--------------------------------
Algorithm:
   1. If head == NULL ‚Üí return NULL.
   2. If k == 1 ‚Üí call `deleteHead()`.
   3. Traverse to (k-1)-th node using a counter.
   4. If `temp->next == NULL` ‚Üí position invalid, print warning.
   5. Otherwise:
        - Store pointer to kth node (`delNode = temp->next`).
        - Bypass it ‚Üí `temp->next = temp->next->next`.
        - Delete `delNode` to free memory.
   6. Return head.

Effect ‚Üí Deletes a node at any position.
Time ‚Üí O(K)
Space ‚Üí O(1)

--------------------------------------------------

(D) Delete Node by Value
-------------------------
Algorithm:
   1. If list empty ‚Üí return NULL.
   2. If head‚Äôs data == value ‚Üí delete head.
   3. Traverse until `temp->next->data == val`.
   4. If not found ‚Üí print ‚ÄúValue not found‚Äù.
   5. Otherwise:
        - Store node to delete.
        - Bypass it and delete the node.
   6. Return head.

Effect ‚Üí Deletes first occurrence of value.
Time ‚Üí O(N)

--------------------------------------------------

üìä 6Ô∏è‚É£ OVERALL COMPLEXITY SUMMARY
--------------------------------------------------
üîπ Insertions:
   - At Head ‚Üí O(1)
   - At Tail ‚Üí O(N)
   - At Kth Position ‚Üí O(K)
   - After Given Value ‚Üí O(N)

üîπ Deletions:
   - At Head ‚Üí O(1)
   - At Tail ‚Üí O(N)
   - At Kth Position ‚Üí O(K)
   - By Value ‚Üí O(N)

üîπ Traversal (printLL) ‚Üí O(N)

üîπ Space Complexity ‚Üí O(1)
   Only pointers are used; no auxiliary data structures.

--------------------------------------------------

üí° 7Ô∏è‚É£ IMPORTANT NOTES
--------------------------------------------------
‚úÖ Always handle edge cases:
   - Empty list (head == NULL)
   - Single node list
   - Invalid position or missing value

‚úÖ Always free memory using `delete` to prevent memory leaks.

‚úÖ Insertion and Deletion change links only ‚Äî no full data shifting.

‚úÖ Linked lists are dynamic structures ‚Äî efficient for insert/delete,
   but slower for random access compared to arrays.

==================================================
*/